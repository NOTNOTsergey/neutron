/*
 * Structure defining a terminal character
 */
typedef struct {
    //The character itself
    char character;
    //The foreground color
    uint8_t fcolor;
    //The background color
    uint8_t bcolor;
    //Reserved for future flags
    uint8_t flags;
} term_char_t;

/*
 * Structure defining terminal X/Y coordinate pair
 */
typedef struct {
    uint16_t x;
    uint16_t y;
} term_coord_t;

/*
 * Sctructure defining a terminal including its state
 */
typedef struct {
    //Terminal ID
    uint32_t id;
    //The display buffer
    term_char_t* buf;
    //The input buffer
    char* input_buf;
    //The input buffer position
    uint16_t input_buf_pos;
    //The cursor position
    term_coord_t cursor;
    //Is the cursor visible or not
    uint8_t cursor_visible;
    //The size of the terminal
    term_coord_t size;
    //The window that's representing the terminal
    window_t* win;
    //The cursor animation progress
    uint8_t cur_anim_prog;
} term_t;

